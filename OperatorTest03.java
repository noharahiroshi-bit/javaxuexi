/*
	关系运算符(比较运算符)：
		>
		>=
		<
		<=
		==
		!=
	所有关系运算符运算结果一定是布尔类型：true/false

	=赋值运算符
	==比较两个值是否相等

*/
public class OperatorTest03{
	public static void main(String[] args){
		
		int a = 10;
		int b = 10;
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a == b);
		System.out.println(a != b);

	}
}